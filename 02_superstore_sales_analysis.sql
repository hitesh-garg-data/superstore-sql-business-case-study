-- Rank products by total sales within each region.
-- The RANK() window function resets ranking for every region
-- using PARTITION BY, allowing comparison of product performance
-- across different geographic markets.

Select
	region,
    product_name,
    Round(sum(sales),0) as total_sales,
    Rank() over (
		partition by region
        order by sum(sales) desc
	) as sales_rank
From
	superstore_data
Group by
	region, product_name;

-- Identify the top 5 best-selling products within each region.
-- The ranking logic is first calculated in a CTE and then filtered
-- to return only the highest-performing products.

With ranked_products As (
	Select
		region,
		product_name,
		Round(sum(sales),0) as total_sales,
		Rank() over (
			partition by region
			order by sum(sales) desc
	) as sales_rank
From
	superstore_data
Group by
	region, product_name
)
Select
	region,
    product_name,
    total_sales,
    sales_rank
From
	ranked_products
where
	sales_rank <= 5
Order by
	region, sales_rank;

-- Calculate month-over-month revenue change using LAG().
-- LAG retrieves revenue from the previous month, allowing
-- calculation of revenue growth or decline over time.

WITH monthly_sales AS (
    SELECT
        YEAR(order_date) AS year,
        MONTH(order_date) AS month,
        Round(SUM(sales),0) AS monthly_revenue
    FROM superstore_data
    GROUP BY YEAR(order_date), MONTH(order_date)
)
SELECT
    year,
    month,
    monthly_revenue,
    Lag(monthly_revenue) Over(
		order by year, month
	) as previous_month_revenue,
    monthly_revenue - Lag(monthly_revenue) over(
		order by year, month
	) As revenue_change
From monthly_sales;

-- Calculate rolling 3-month revenue using a window frame.
-- The query sums revenue for the current month and the
-- previous two months to smooth short-term fluctuations
-- and reveal broader revenue trends.

WITH monthly_sales AS (
    SELECT
        YEAR(order_date) AS year,
        MONTH(order_date) AS month,
        Round(SUM(sales),0) AS monthly_revenue
    FROM superstore_data
    GROUP BY YEAR(order_date), MONTH(order_date)
)
SELECT
    year,
    month,
    monthly_revenue,
    SUM(monthly_revenue) OVER (
        ORDER BY year, month
        ROWS BETWEEN 2 PRECEDING AND CURRENT ROW
    ) AS rolling_3_month_revenue
FROM monthly_sales;
    
