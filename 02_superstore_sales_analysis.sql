-- Identify customers whose total sales exceed
-- the average sales generated by all customers.
-- The inner subquery calculates total sales per customer,
-- then compares each customer's sales with the overall average.

Select
		customer_id,
        customer_name,
        Round(sum(sales) ,2) as total_sales
From	
		superstore_data
Group by
		customer_id,
        customer_name
Having 
		Sum(sales) > (
        Select 
				Avg (customer_sales)
		From (
				Select sum(sales) as customer_sales
                From superstore_data
                Group by customer_id
			)t
)
order by
		total_sales Desc;

-- Identify products generating higher-than-average profit.
-- The query aggregates profit per product and filters only
-- those products whose total profit exceeds the average
-- profit generated by all products.

Select
		product_id,
        product_name,
        Round(sum(profit) ,2) as total_profit
From	
		superstore_data
Group by
		product_id,
        product_name
Having 
		Sum(profit) > (
        Select 
				Avg (product_profit)
		From (
				Select sum(profit) as product_profit
                From superstore_data
                Group by product_id
			)t
)
order by
		total_profit Desc;
  
-- Rewrite the above customer sales query using CTEs.
-- The first CTE calculates total sales for each customer,
-- while the second CTE calculates the average sales across customers.

With customer_sales As (
		Select
			customer_id,
            customer_name,
            Sum(sales) as total_sales
		From
			superstore_data
		group by
			customer_id,
            customer_name
),
	average_sales As (
		Select
			avg(total_sales) as avg_sales
		From
			customer_sales
)
Select
	customer_id,
	customer_name,
    Round(total_sales, 2) As total_sales
From
	customer_sales
Where
	total_sales > (Select avg_sales from average_sales)
ORDER BY 
	total_sales DESC;
    
-- Rewrite the above product profitability query using CTEs.
-- This improves query readability by separating calculation
-- of product profits and average profit into logical layers.
