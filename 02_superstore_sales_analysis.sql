-- =========================================
-- SELF JOIN ANALYSIS
-- Identify clusters of high-value customers located in the same city.
-- The query first selects the top 50 customers by total sales and then
-- performs a self join to pair customers who operate within the same city.
-- This helps reveal geographic concentrations of important clients.
-- =========================================


SELECT
    a.customer_id as customer_1,
	a.customer_name AS name_1,
    b.customer_id AS customer_2,
    b.customer_name AS name_2,
    a.city
FROM
(
	-- Subquery A: Select the top 50 customers by total sales
    -- GROUP BY ensures each customer appears once
    SELECT customer_id, customer_name, city
    FROM superstore_data
    GROUP BY customer_id, customer_name, city
    ORDER BY SUM(sales) DESC
    LIMIT 50
) a
JOIN
(
	-- Subquery B: Same top 50 customers used again to compare customers
    -- against each other within the same city
    SELECT customer_id, customer_name, city
    FROM superstore_data
    GROUP BY customer_id, customer_name, city
    ORDER BY SUM(sales) DESC
    LIMIT 50
) b
ON a.city = b.city
AND a.customer_id < b.customer_id;

-- The condition a.customer_id < b.customer_id prevents:
-- 1. matching a customer with themselves
-- 2. duplicate pairs such as (A,B) and (B,A)

-- =========================================
-- CTE REWRITE PRACTICE
-- Rewriting the previous self join using a Common Table Expression (CTE).
-- This approach improves readability and avoids repeating the same
-- subquery multiple times.
-- =========================================

With top_customers AS (
	select
		customer_id,
        customer_name,
        city,
        Round(sum(sales) ,2) as total_sales
	from superstore_data
    Group by customer_id, customer_name, city
    Order by total_sales DESC
    Limit 50
)
Select
	a.customer_id as customer_1,
	a.customer_name AS name_1,
    b.customer_id AS customer_2,
    b.customer_name AS name_2,
    a.city
From top_customers a
Join top_customers b
On a.city = b.city
And a.customer_id < b.customer_id;

-- This CTE version performs the same analysis as SELF JOIN ANALYSIS,
-- but structures the logic more clearly by defining the
-- "top_customers" dataset once and reusing it in the join.

