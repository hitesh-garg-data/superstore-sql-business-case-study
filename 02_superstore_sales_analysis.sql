WITH monthly_profit AS (
    SELECT
        YEAR(order_date) AS year,
        MONTH(order_date) AS month,
        Round(SUM(profit),0) AS monthly_profit
    FROM superstore_data
    GROUP BY YEAR(order_date), MONTH(order_date)
)
SELECT
    year,
    month,
    monthly_profit,
    LAG(monthly_profit) OVER (
        ORDER BY year, month
    ) AS previous_profit,
    monthly_profit - LAG(monthly_profit) OVER (
        ORDER BY year, month
    ) AS profit_change
FROM monthly_profit;

-- High-performing products vary significantly across regions and years,
-- indicating that product demand is geographically and temporally dynamic.
-- Additionally, several products generate strong revenue but weak or
-- negative profit margins, highlighting the importance of analyzing
-- profitability alongside sales performance when making business decisions.